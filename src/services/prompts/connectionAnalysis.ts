// 章节关联分析相关的prompt模板

export const getChapterConnectionsAnalysisPrompt = (chapterSummaries: string) => {
  const userPrompt = `请帮我分析这本书各章节之间的关系，并总结全书的核心内容：

${chapterSummaries}

请从以下几个方面来分析：

## 1. 章节之间的联系
- 各章节是如何一步步展开论述的？
- 哪些重要观点在不同章节中反复出现？
- 前面的章节为后面的内容做了哪些铺垫？

## 2. 全书的核心主题
- 这本书最想告诉读者什么？
- 作者的主要观点是什么？
- 有哪些值得特别关注的重要概念？

## 3. 实用价值
- 这本书对现实生活有什么指导意义？
- 读者可以从中学到什么实用的知识或方法？
- 哪些观点可能会改变我们的思考方式？

## 4. 简明总结
- 用几句话概括这本书的精华
- 推荐给什么样的人阅读
- 阅读这本书的最大收获是什么

请用通俗易懂的语言来分析，让普通读者也能轻松理解书籍的价值和意义。`
  
  return userPrompt
}

